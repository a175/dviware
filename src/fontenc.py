class FontAttribute:
    def __init__(self,family,series,weight,width,shape,variant,glyphset,encoding):
        """
        family:typeface name,
        series:combination of weight and width
        weght:regular, bold, semibold, light, demi, book, medium, black, etc.
        width:normal, condensed, compressed, extended, expanded, etc.
        shape:normal (upright), slanted (oblique), italic
        variant:small caps, old style figures
        glyphset:standard, expert, alternate, swash, etc.
        encoding:OT1, T1, OML, JIS.etc
        """
        self.family=family
        self.series=series
        self.weight=weight
        self.width=width
        self.shape=shape
        self.variant=variant
        self.glyphset=glyphset
        self.encoding=encoding
    def is_bold(self):
        if self.weight == "bold":
            return True
        else:
            return False

    def is_italic(self):
        if self.shape=="italic":
            return True
        else:
            return False

    def is_smallcaps(self):
        if self.variant=="smallcaps":
            return True
        else:
            return False
        
    def get_unicode(self,c):
        if self.encoding == "T1":
            return T1[c]
        if self.encoding == "OT1":
            return OT1[c]
        if self.encoding == "OML":
            return OML[c]
        if self.encoding == "OMS":
            return OMS[c]
        if self.encoding == "U":
            return chr(c)
        if self.encoding == "JIS":
            return JIS[c]
        else:
            return "[??"+self.family+":"+str(c)+"]"
        
